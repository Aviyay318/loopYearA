public class Ex1 {
//    תרגיל 1:
//    כתבו תוכנית המדפיסה את כל המספרים הטבעיים מ-0 ועד 1000.

    public static void main(String[] args) {
        //סדר עולה מ0 עד 1000
        for (int i = 0; i <=1000; i++) {
            System.out.println(i);
        }
        System.out.println("******************");
        //סדר יורד מ1000 עד 0
        for (int i=1000; i>=0 ; i--){
            System.out.println(i);
        }
    }
}
